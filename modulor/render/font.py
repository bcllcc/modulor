_UNKNOWN = [[(0.08, 0), (0.52, 0), (0.52, 1), (0.08, 1), (0.08, 0)]]


def glyph(ch: str):
    if ch in _G:
        return _G[ch]
    up = ch.upper()
    if up in _G:
        return _G[up]
    return _UNKNOWN


def text_width(text: str, height: float) -> float:
    if not text:
        return 0.0
    return (len(text) * ADVANCE - (ADVANCE - GLYPH_W)) * height


def text_strokes(text: str, at, height: float, rotation: float = 0.0,
                 anchor: str = "start") -> list[list[list[float]]]:
    """Expand text to world-space polylines."""
    a = math.radians(rotation)
    c, s = math.cos(a), math.sin(a)
    ox, oy = float(at[0]), float(at[1])
    if anchor in ("middle", "end"):
        w = text_width(text, height)
        k = 0.5 if anchor == "middle" else 1.0
        ox -= w * k * c
        oy -= w * k * s
    strokes = []
    for i, ch in enumerate(text):
        dx = i * ADVANCE * height
        for poly in glyph(ch):
            pts = []
            for gx, gy in poly:
                x = (gx * height + dx)
                y = gy * height
                pts.append([ox + x * c - y * s, oy + x * s + y * c])
            strokes.append(pts)
    return strokes
